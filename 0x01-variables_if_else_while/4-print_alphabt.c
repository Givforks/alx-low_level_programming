#include <stdio.h>
/**
 * main - A main function entry point program break and continue loop
 * Return: 0 the return (completed)
 */
int main(void)
{
	char l;

	l = 'a';
		/* declear variable and use while loop with two putchar */
		while
			(l <= 'z')
				{
			if ((l != 'q' && l != 'e') && l <= 'z')
			putchar(l);
			l++;
				}
		putchar('\n');
			return (0);
}


