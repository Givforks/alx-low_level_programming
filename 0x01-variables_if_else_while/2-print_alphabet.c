#include <stdio.h>
/**
 * main - Entry point body function program void
 * Return: Everytime return 0 (Completed)
 */
int main(void)
{
		char t;
		/*  var_t,'' quote will locate ascii char decimal value */
		t = 'a';
		while
			(t <= 'z')
				{
					putchar(t);
					t++;
				}

		putchar('\n');
		return (0);
}
